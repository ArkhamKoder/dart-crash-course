void main(List<String> args) {
  // Binary infix
  const age = 50;
  print(age + 20);
  print(age - 20);
  print(age * 20);
  print(age / 20);
  print(age ~/ 20);
  print(age == 20);
  print(age != 20);
  print(age > 20);
  print(age < 20);
  print(age >= 20);
  print(age <= 20);
  print(age);
  // bitwise infix operators

  print(age & 20); // bitwise AND
  print(age | 20); // bitwise OR
  print(age ^ 20); // bitwise XOR

  print(age << 20); //bitwise left
  print(age >> 20); //bitwise right
}
